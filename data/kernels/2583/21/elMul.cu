__global__ void elMul(int N, int M, float *X1, float *X2)
{


int i = blockIdx.x * blockDim.x + threadIdx.x;
int j = blockIdx.y * blockDim.y + threadIdx.y;

int index = j*N + i;

if (i < N && j < M)
{
X1[index] = __fmul_rn(X1[index], X2[index]);
}
}