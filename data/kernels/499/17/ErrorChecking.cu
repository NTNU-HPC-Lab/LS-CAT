#pragma once
#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct ErrorChecking{

	bool static dbg;

	__host__ static inline int hostErrorCheck(cudaError_t,const char*);
	__device__ static inline int deviceErrorCheck(cudaError_t,const char*);

	__host__ static inline void hostMessage(const char*);
	__device__ static inline void deviceMessage(const char*);

	__host__ static inline void hostError(const char*);
	__device__ static inline void deviceError(const char*);	
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool ErrorChecking::dbg = true;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

__host__ inline int ErrorChecking::hostErrorCheck(cudaError_t code, const char* msg){
   if (code != cudaSuccess) {
      printf("\033[31mHOST GPU ERROR: %s ,%s\033[0m\n", cudaGetErrorString(code), msg);
      return -1;
   }
   return 0;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

__device__ inline int ErrorChecking::deviceErrorCheck(cudaError_t code, const char *msg){
   if (code != cudaSuccess) {
      printf("\033[31mDEVICE GPU ERROR: %s ,%s\033[0m\n", cudaGetErrorString(code), msg);
      return -1;
   }
   return 0;
}

