__host__ inline void ErrorChecking::hostMessage(const char* msg){
	//printf("\033[34m%s\033[0m\n", msg);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

__device__ inline void ErrorChecking::deviceMessage(const char* msg){
	//printf("\033[34m%s\033[0m\n", msg);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

__host__ inline void ErrorChecking::hostError(const char* msg){
	printf("\033[31m%s\033[0m\n", msg);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

__device__ inline void ErrorChecking::deviceError(const char* msg){
	printf("\033[31m%s\033[0m\n", msg);
}

